const char* micro_key_filter(int key)
{
    if(key >= GLFW_KEY_SPACE && key <= GLFW_KEY_GRAVE_ACCENT)
        return key_names1[key - GLFW_KEY_SPACE];
    else if(key >= GLFW_KEY_ESCAPE && key <= GLFW_KEY_F25)
        return key_names2[key - GLFW_KEY_ESCAPE];
    else
        return NONE;
}